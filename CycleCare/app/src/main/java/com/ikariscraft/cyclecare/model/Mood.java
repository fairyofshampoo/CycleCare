package com.ikariscraft.cyclecare.model;

public class Mood {
    private int moodId;
    private String name;

    public Mood() {
    }

    public int getMoodId() {
        return moodId;
    }

    public void setMoodId(int moodId) {
        this.moodId = moodId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
